public class SimpleFunctions {

    public int add(int a, int b){
        return a + b;
    }

    public int subract(int a, int b){
        return a - b;
    }

    public int multiply(int a, int b){
        return a * b;
    }

    public int divide(int a, int b){
        return a / b;
    }

    public String concat(String a, String b){
        return a+b;
    }

}
